#include "../libft.h"

int	ft_is_in_set(char c, char *set)
{
	int	i;

	i = 0;
	if (set == NULL)
		return (0);
	while (set[i] != '\0')
	{
		if (c == set[i])
			return (1);
		i++;
	}
	return (0);
}
